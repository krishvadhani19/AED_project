    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String userName = txtUserName.getText();
        char[] passwordArray = txtPassword.getPassword();
        String password = String.valueOf(passwordArray);

        //Step1: Check in the system admin user account directory if you have the user
        //        System.out.println(system.getName());
        //        System.out.println("1");
        //        System.out.println(system.getUserAccountDirectory());
        //        System.out.println("2");
        //        System.out.println(userName);
        //        System.out.println(password);
        UserAccount userAccount = system.getUserAccountDirectory().authenticateUser(userName, password);

        Enterprise inEnterprise = null;
        Organization inOrganization = null;
        Network inNetwork = null;

        if (userAccount == null) {
            //Step 2: Go inside each network and check each enterprise
            for (Network network : system.getNetworkList()) {
                //Step 2.a: check against each enterprise
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                    if (userAccount == null) {
                        //Step 3:check against each organization for each enterprise
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if (userAccount != null) {
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                inNetwork = network;
                                break;
                            }
                        }

                    } else {
                        inEnterprise = enterprise;
                        break;
                    }
                    if (inOrganization != null) {
                        break;
                    }
                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }

        if (userAccount == null) {
            JOptionPane.showMessageDialog(null,
                new JLabel("<html><b>" + " Invalid Credentials!</b></html>"),
                "Error",
                JOptionPane.ERROR_MESSAGE
            );

            //JOptionPane.showMessageDialog(null, "Invalid credentials!");
            return;
        } else {
            //CardLayout layout = (CardLayout) rightJPanel.getLayout();
            System.out.println(inEnterprise);
            System.out.println(userAccount.getRole());
            System.out.println(system);
            
            CardLayout layout = (CardLayout) mainProcessContainer.getLayout();
            JPanel loggedInWorkArea = userAccount.getRole().createWorkArea(mainProcessContainer, userAccount, inOrganization, inEnterprise, system, inNetwork);
            
            MainWorkArea mainWorkArea = new MainWorkArea(system, mainProcessContainer, loggedInWorkArea);
            mainProcessContainer.add("MainWorkArea", mainWorkArea);
            layout.next(mainProcessContainer);
        }

    }//GEN-LAST:event_btnLoginActionPerformed

